Obstacle::Obstacle(sf::Vector2f position)
{

    for (unsigned int row = 0; row < grid.size(); ++row)
        for (unsigned int column = 0; column < grid[0].size(); ++column)
        {
            if (grid[row][column] == 1)
            {
                float posX = position.x + column * 3;
                float posY = position.y + row * 3;
                Block block = Block({posX, posY});
                blocks.push_back(block);
            }
        }
}

void Obstacle::render(sf::RenderTarget *target)
{
    for (auto &block : blocks)
    {
        block.render(target);
    }
}
